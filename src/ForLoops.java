public class ForLoops {

    public static void main(String[] args) {

        /*
        with in a for loop there are a total of 3 statments a index you declare, a condition and a incrament
         */

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }

    }

}
